//1. Operadores Aritméticos

/* 
+   suma
-   resta
*   multiplicación
/   división
%   módulo (residuo)
**  potencia 
*/


//2. Operadores de Asignación
/* 
=    asignación
+=   suma y asigna
-=   resta y asigna
*=   multiplica y asigna
/=   divide y asigna
%=   módulo y asigna
**=  potencia y asigna
*/

//3. Operadores de Comparación
/* 
==   igual (valor)
===  igual estricto (valor + tipo)
!=   diferente
!==  diferente estricto
>    mayor que
<    menor que
>=   mayor o igual
<=   menor o igual
*/

//4. Operadores Lógicos
/* 
&&   AND (y)
||   OR (o)
!    NOT (negación)
*/

